/**
 * 获取单个字符的拼音首字母；非汉字返回小写字母自身（ASCII）或空串
 */
function getCharInitial(ch: string): string {
  const code = ch.codePointAt(0) || 0
  // ASCII 字母
  if ((code >= 0x41 && code <= 0x5A) || (code >= 0x61 && code <= 0x7A)) {
    return ch.toLowerCase()
  }
  // 数字
  if (code >= 0x30 && code <= 0x39) return ch
  // 特殊字符表查找
  if (SPECIAL_MAP[ch]) return SPECIAL_MAP[ch]
  // CJK 范围用边界表二分查找
  if (code >= 0x4E00 && code <= 0x9FA5) {
    let initial = 'z'
    for (let i = 0; i < PINYIN_BOUNDARIES.length; i++) {
      if (code < PINYIN_BOUNDARIES[i][0]) {
        initial = i === 0 ? 'a' : PINYIN_BOUNDARIES[i - 1][1]
        return initial
      }
    }
    return 'z'
  }
  return ''
}

/**
 * 提取字符串的拼音首字母序列
 * 例：'王严萱' → 'wyx'；'Alice' → 'alice'；'测试123' → 'cs123'
 */
export function getPinyinInitials(text: string): string {
  if (!text) return ''
  let out = ''
  for (const ch of text) {
    const initial = getCharInitial(ch)
    if (initial) out += initial
  }
  return out
}

/**
 * 判断关键词 kw 是否命中 target 的首字母序列
 * 支持：kw 为纯字母时匹配首字母（如 wyx 匹配 王严萱）
 */
export function matchPinyin(target: string, kw: string): boolean {
  if (!target || !kw) return false
  const lowerKw = kw.toLowerCase()
  if (!/^[a-z0-9]+$/.test(lowerKw)) return false
  const initials = getPinyinInitials(target)
  return initials.includes(lowerKw)
}
